package com.app.chatbot.dao.chat;

public class StatementDao {

    private String statement;

    private String expectations;

    private String reservedStatement;

    public String getStatement() {
        return statement;
    }

    public void setStatement(String statement) {
        this.statement = statement;
    }

    public String getExpectations() {
        return expectations;
    }

    public void setExpectations(String expectations) {
        this.expectations = expectations;
    }

    public String getReservedStatement() {
        return reservedStatement;
    }

    public void setReservedStatement(String reservedStatement) {
        this.reservedStatement = reservedStatement;
    }

}
